public class ASR {
	
	String applicationNumber;
	String eCTD;
	String coreID;
	String contactInfo;
	String techAssistant;
	String other;
	
	public String getApplicationNumber() {
		return applicationNumber;
	}
	public void setApplicationNumber(String applicationNumber) {
		this.applicationNumber = applicationNumber;
	}
	public String geteCTD() {
		return eCTD;
	}
	public void seteCTD(String eCTD) {
		this.eCTD = eCTD;
	}
	public String getCoreID() {
		return coreID;
	}
	public void setCoreID(String coreID) {
		this.coreID = coreID;
	}
	public String getContactInfo() {
		return contactInfo;
	}
	public void setContactInfo(String contactInfo) {
		this.contactInfo = contactInfo;
	}
	public String getTechAssistant() {
		return techAssistant;
	}
	public void setTechAssistant(String techAssistant) {
		this.techAssistant = techAssistant;
	}
	public String getOther() {
		return other;
	}
	public void setOther(String other) {
		this.other = other;
	}

}
